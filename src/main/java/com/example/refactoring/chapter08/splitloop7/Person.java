package com.example.refactoring.chapter08.splitloop7;

public class Person {

    private int age;
    private int salary;

    public Person(int age, int salary) {
        this.age = age;
        this.salary = salary;
    }

    public int getAge() {
        return age;
    }

    public int getSalary() {
        return salary;
    }
}
